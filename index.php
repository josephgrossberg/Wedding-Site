    <h2 id="our_story">Our Story</h2>

    <p><img src="joe_and_salem.jpg" alt="Joe and Salem in NC" /><br />
    <cite>Salem and Joe announce their engagement to Salem's family in Raleigh, North Carolina, on December 24, 2008.</cite></p>

    <p>Joe and Salem met on <a href="http://www.jdate.com">JDate</a>, an online Jewish dating site, in August 2006. Salem had been going on a lot of really bad first dates, so, even though Joe seemed like a nice guy, she decided that she wanted backup for her next outing. Their first date, then, was to watch the first Texas football game of the season at <a href="http://rhodesidegrill.homestead.com/">Rhodeside Grill</a> in Arlington, Virginia, with her friends Brian Moulton and Austin Fulk &mdash; and a hundred or so burnt orange-clad members of the Washington, D.C. Texas Exes Association.</p>

    <p>Joe was smitten from day one, but Salem was a bit more stubborn. On the way back from a trip to New York with her mother, Salem texted with her friend Larry Schooler about her new relationship. Larry made the outrageous suggestion that she tell Joe that she liked him. Salem, still uncertain about her readiness for romance, responded with the very mature, "I do not!" But she did, and she told Joe so. Salem soon fell in love with Joe &mdash; and his wonderful cats, Miju and Peeps.</p>

    <p><img src="miju_and_peeps.jpg" alt="Miju and Peeps" /><br />
    <cite>Miju (L) and Peeps, being awesome.</cite></p>

    <p>In April 2008, they moved into their current downtown apartment, and Salem achieved her dream of living with Peeps. In December, Joe and Salem decided to get married.</p>

    <p>October 25 will be not only the three-year anniversary of when we started dating, but also the seventh anniversary of our friends Brian and Austin, who accompanied us on that very first date.</p>

